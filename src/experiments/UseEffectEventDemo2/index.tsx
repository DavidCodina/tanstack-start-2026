///////////////////////////////////////////////////////////////////////////
//
// The official React demo for useEffectEvent is more complex than it needs to be.
// Here is a very simple example.
//
// Use Case: You want to call a prop callback once on mount, but you don't want to
// force every consumer to remember to memoize it. This is defensive programming
// at the component library level.
//
///////////////////////////////////////////////////////////////////////////s

export const UseEffectEventDemo2 = () => {
  const [count, setCount] = React.useState(0)
  const countRef = React.useRef(count)

  const onMountedCallback = () => {
    // ⚠️ Gotcha: While useEffectEvent ensures ChildComponent doesn't re-trigger effects
    // when the callback changes, it doesn't solve the stale closure inside the
    // setTimeout within onMountedCallback.
    // ❌ setTimeout(() => { console.log(`\n\nThe component mounted! The count is: ${count}\n`) }, 5000)

    setTimeout(() => {
      console.log(
        `\n\nThe component mounted! The count is: ${countRef.current}\n`
      )
    }, 5000)
  }

  // The consumer SHOULD do this as a best practice, but now we
  // no longer need to trust that they will.

  // const onMountedCallback = React.useCallback(() => {
  //   console.log('\n\nThe component mounted!\n\n')
  // }, [])

  return (
    <>
      <Button
        className='mx-auto my-6 flex min-w-[150px]'
        // onClick={() => { setCount((v) => v + 1) }}
        onClick={() => {
          setCount((v) => {
            const newValue = v + 1
            countRef.current = newValue
            return newValue
          })
        }}
        size='sm'
      >
        Parent Clicks: {count}
      </Button>

      <ChildComponent onMountedCallback={onMountedCallback} />
    </>
  )
}
