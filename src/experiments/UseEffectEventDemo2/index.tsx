'use client'

import * as React from 'react'
import { Button } from '@/components'

const ChildComponent = ({
  onMountedCallback
}: {
  onMountedCallback: () => void
}) => {
  const [count, setCount] = React.useState(0)

  // Concise syntax: const onMounted = React.useEffectEvent(onMountedCallback)
  const onMounted = React.useEffectEvent(() => {
    // Note: Within the actual useEffectEvent callback's function body,
    // this function bod can always "SEE" state outside of it, in the scope
    // of the surrounding component.
    // console.log('Child Clicks:', count)
    onMountedCallback()
  })

  ///////////////////////////////////////////////////////////////////////////
  //
  //
  // This is bad because onMountedCallback will be created anew in the parent
  // component on every render. And every new instance will re-trigger the useEffect.
  // ❌ React.useEffect(() => { onMountedCallback() }, [onMountedCallback])
  //
  // The issue is exactly the same here.
  // ❌  const onMounted = React.useCallback(() => { onMountedCallback() }, [onMountedCallback])
  //
  // If we did this in the parent component, it would work:
  //
  //   const onMountedCallback = React.useCallback(() => { console.log('\n\nThe component mounted!\n\n') }, [])
  //
  // Then we could do this here without re-triggering the useEffect every time the parent re-rendered.
  //
  //   React.useEffect(() => { onMountedCallback() }, [onMountedCallback])
  //
  // However, we probably don't want to trust that the consuming parent will always do the right thing.
  //
  // The legacy solution was either to omit the callback from the useEffect deps and disable linting, or
  // wrap the callback in a ref to get around the deps. With useEffectEvent, we now have a more official
  // (less hacky) solution.
  //
  ///////////////////////////////////////////////////////////////////////////

  React.useEffect(() => {
    // setTimeout(() => {
    onMounted()
    // }, 2000)
  }, [])
  ///////////////////////////////////////////////////////////////////////////
  //
  // Note: If for some reason your dependency array is still requiring onMounted,
  // then make sure you're ESLint React plugins are up-to-date.
  //
  // ⚠️ Gotcha: Sometimes useEffectEvent allows you to omit ALL dependencies!
  // In some cases the function generated by useEffectEvent may contain
  // all logic for the useEffect and technically you may not need to
  // add anything to the dependency array. In that case, make sure you're not
  // omitting dependencies that you actually want in the array to trigger the effect.
  // This is almost like React went too far in the other direction. Thus, even though
  // the linter warning for missing dependencies still exists, we can now once again
  // inadvertently omit dependencies! Yay!
  //
  ///////////////////////////////////////////////////////////////////////////

  return (
    <Button
      className='mx-auto my-6 flex min-w-[150px]'
      onClick={() => {
        setCount((v) => v + 1)
      }}
      size='sm'
      variant='secondary'
    >
      Child Clicks: {count}
    </Button>
  )
}

